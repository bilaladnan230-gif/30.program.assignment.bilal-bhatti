#include <iostream>
using namespace std;

int main() {
    // Assigning values at the time of declaration
    int a = 10;
    float b = 5.5;
    char c = 'A';

    // Printing the assigned values
    cout << "Value of a: " << a << endl;
    cout << "Value of b: " << b << endl;
    cout << "Value of c: " << c << endl;

    return 0;
    
}
